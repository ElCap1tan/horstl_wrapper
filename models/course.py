class Course:

    def __init__(self, name: str, kind: str, group: str, time: str, frequency: str, time_period: str, room_info: str, docent: str, status: str, warning: str):
        self.name = name
        self.kind = kind
        self.group = group
        self.time = time
        self.frequency = frequency
        self.time_period = time_period
        self.room_info = room_info
        self.docent = docent
        self.status = status
        self.warning = warning
